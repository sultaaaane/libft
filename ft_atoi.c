#include "libft.h"

int	ft_atoi(const char *str)
{
	int				i;
	unsigned long	result;
	int				sign;

	sign = 1;
	i = 0;
	result = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + (str[i] - '0');
		if (ft_strlen(str) > (~((size_t)0)) && sign == -1)
			return (0);
		else if (ft_strlen(str) > (~((size_t)0)) && sign == 1)
			return (-1);
		i++;
	}
	return (result * sign);
}
