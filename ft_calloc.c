#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*str;

	if (nmemb == 0 || size == 0)
	{
		return (NULL);
	}
	str = malloc((nmemb) * size);
	ft_bzero(str, nmemb);
	return (str);
}
